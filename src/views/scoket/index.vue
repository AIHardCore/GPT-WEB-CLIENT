<template>
  <div class="contents"
    v-loading="loading">
    <div class="body">
      <Notice :notice="notice"
        @open="open">
      </Notice>
      <div class="main_home">
        <div class="left"
          v-if="!phone">
          <Menu
            @addList="addList"
            :total="totals"
            @changeChat="changeChat"
            :kit-list="kitList"
            :chat-list="chatLists"
            :user-info="userInfo">
          </Menu>
        </div>
        <div
          class="chat_right" ref="chatScroll" v-on:wxj="updateLoadState">
          <Content
            :isChat="isChat"
            :isChats="isChats"
            :chatList="chatLists">
          </Content>
        </div>
      </div>
      <div>
        <div class="tool"
          :class="{'phone':phone}">
          <el-tooltip
            class="item"
            effect="dark"
            content="即时通讯"
            placement="top-start">
            <img
              :class="{'active':isActive == 0}"
              src="../../assets/chats_icon.png"
              class="icon">
          </el-tooltip>
          <el-tooltip
            class="item"
            effect="dark"
            content="连续通讯"
            placement="top-start">
            <img
              @click="changeChats(1)"
              src="../../assets/chat_icon.png"
              class="icon">
          </el-tooltip>
          <el-tooltip
            class="item"
            effect="dark"
            content="画图"
            placement="top-start">
            <img
              @click="changeChats(3)"
              src="../../assets/picture.png"
              class="icon">
          </el-tooltip>
          <el-tooltip
            class="item"
            effect="dark"
            content="充值"
            placement="top-start">
            <img
              @click="changeChats(2)"
              src="../../assets/pay_2.png"
              class="icon">
          </el-tooltip>
          <span
            style="right: 30px;position: absolute;"
            :style="{color:color}">
            <i v-if="color == 'green'"
              class="el-icon-circle-check"
              style="margin-right:5px;font-size: 14px;"></i>
            <i v-if="color == 'red'"
              class="el-icon-circle-close"
              style="margin-right:5px;font-size: 14px;"></i>
            {{ scoketText }}</span>
        </div>
        <SendText
          @sendText="sendTexts"
          @ok="getData"
          @total="total">
        </SendText>
      </div>
    </div>
